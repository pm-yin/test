// 👉 可自訂參數
const studentCount = 23; // 你可以調整學生人數
const combosPerStudent = 4; // 每位學生抽幾組組合

// 抽籤邏輯
document.getElementById("drawButton").addEventListener("click", () => {
  const list = document.getElementById("resultList");
  list.innerHTML = "";

  for (let i = 0; i < studentCount; i++) {
    const title = document.createElement("li");
    title.innerHTML = `<strong>👩‍🎨 學生 ${i + 1}</strong>`;
    list.appendChild(title);

    for (let j = 0; j < combosPerStudent; j++) {
      const A = emotions[Math.floor(Math.random() * emotions.length)];
      const B = actions[Math.floor(Math.random() * actions.length)];
      const C = objects[Math.floor(Math.random() * objects.length)];

      const li = document.createElement("li");
      li.textContent = `➤ ${A}${B}${C}`;
      list.appendChild(li);
    }

    // 空行分隔
    const space = document.createElement("li");
    space.innerHTML = "<br/>";
    list.appendChild(space);
  }
});
